 <div class="row">
  <div class="large-3 columns"><h2>Home care tips</h2></div>
  <div class="large-9 columns">
    <p>
 Daily care includes keeping feet clean and dry and skin soft and smooth. One should conduct daily inspection for red skin spots, cuts, sores, swelling or infected toe nails.  Also avoid extreme heat or cold on the feet, use sunscreen and wear shoes at the beach; keeping toe nails trimmed and feet free of callouses and corns. Do not cut corns or calluses or use plaster or liquid removers.      
    </p>
  </div>
 </div>

<div class="row">
  <div class="large-3 columns"><h2>Shoes and socks</h2></div>
  <div class="large-9 columns">
    <p>
Ill-fitting shoes or socks can lead to blisters, a major concern for podiatrists. The National Diabetes Education program suggest wearing soft lightly padded, seamless socks, wearing walking or athletic shoes that allow room for your toes. Never wear vinyl or plastic shoes and purchase new shoes at the end of the day when our feet are largest for the best fit.    
    </p>
